SELECT * FROM
    (
    SELECT DISTINCT 
        LEGACY_ID, LAST_UPDATED, STATION_ID, NUM_EBIKES_AVAILABLE, NUM_EBIKES_AVAILABLE_BOOL, NUM_BIKES_AVAILABLE, NUM_DOCKS_AVAILABLE, LAST_REPORTED, STATION_STATUS, IS_INSTALLED, NUM_DOCKS_DISABLED, NUM_BIKES_DISABLED, IS_RENTING, IS_RETURNING, EIGHTD_HAS_AVAILABLE_KEYS  FROM DIVVY_DATABASE.PUBLIC.STATION_STATUS_FLATTEN_FULL 
        WHERE LEGACY_ID = '185' 
        OR LEGACY_ID = '222' 
        OR LEGACY_ID = '125' 
        or LEGACY_ID = '196' 
        or LEGACY_ID = '47'
        or LEGACY_ID = '285'
        or LEGACY_ID = '316'
        or LEGACY_ID = '116'
        ORDER BY LAST_UPDATED desc
        LIMIT 11520 
    )
ORDER BY LEGACY_ID asc, LAST_UPDATED asc;